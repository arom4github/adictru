<h2>Инструментарий СИБАС: прямой и обратный словари, подкорпусы, экспертное приложение, разметка </h2>
<p><i>Словари</i>. Представленные в СИБАС в открытом доступе эмпирические материалы
могут использоваться автономно в режиме прямого и обратного словаря. Для этого
на сайте имеется необходимый инструментарий (см. <a href="dictdescr">описание словаря</a> 
и  <a href="stimlist">список стимулов</a>). 

<p><i>Экспертное приложение</i>. С 2019 года ведется разработка и отладка экспертного
обучающего приложения к СИБАС. Работы по оптимизации СИБАС, включающие в себя
создание и отладку Приложения, ряда Подкорпусов и их апробацию в Приложении,
осуществление исследовательских работ на основе полученных в целевых локальных
экспериментах данных, при их обработке с использованием Приложения и всего
инструментария СИБАС, а также комплекс работ по разметке СИБАС с 2019 года
проводятся в рамках проекта Института филологии СО РАН «Культурные универсалии
вербальных традиций народов Сибири и Дальнего Востока: фольклор, литература,
язык» по гранту Правительства РФ для государственной поддержки научных
исследований, проводимых под руководством ведущих ученых (соглашение №
075-15-2019-1884).

<p>Приложение разработано и подготовлено И.В.Шапошниковой (техническое
сопровождение – Н.Ю.Зятьков). Экспертное обучающее приложение представляет
собой программу для предварительного статистического анализа представленных в
СИБАС эмпирических материалов. С его помощью пользователь может анализировать
любое из ассоциативных полей, имеющихся в прямом или обратном словарях
основного корпуса и подкорпусов СИБАС. Анализ позволяет получить психоглоссы
разного типа в зависимости от поставленных пользователем задач. Приложение
оснащено справочными материалами: руководством по выявлению психоглосс и
глоссарием терминов. Видеоинструкция по приложению – <a href="/media/db/SIBAS_Application.mp4">здесь</a>. 

