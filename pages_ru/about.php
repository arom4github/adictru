<p><h2>СИБАС - Русская региональная ассоциативная база данных (Сибирь и Дальний Восток) - 2008-2022 гг.</h2>
<p>Русская региональная ассоциативная база данных (СИБАС), собираемых в
азиатских регионах РФ (в Сибири и на Дальнем Востоке), представляет собой
продукт реализации комплексных междисциплинарных проектов, направленных на
исследование русского языкового сознания. Базовая часть этих проектов
выполняется исследовательской группой в секторе русского языка в Сибири
Института филологии СО РАН и НГУ в сотрудничестве с академическими институтами
(преимущественно ИЯз РАН), центрами и лабораториями, а также заинтересованными
вузами России.
 
<h2>Описание СИБАС</h2>
<p>СИБАС содержит обновляемый и пополняемый <b>основной корпус</b> и <b>подкорпусы</b>.

<p>Основной корпус ассоциаций создаётся как пополняемая эмпирическая база для
построения ассоциативно-вербальной модели русской языковой личности –
студенческой молодёжи Сибири и Дальнего Востока XXI века. Наличие такой
эталонной для азиатских регионов РФ базы данных создает условия для изучения
активной грамматики и флуктуаций психологически актуальных смыслов как по
временно́й оси («по вертикали»), так «по горизонтали» – в сравнении с данными
ассоциативных баз других регионов России. Кроме того, массовый характер и
значительный охват по времени проводимых экспериментов позволяет не только
исследовать динамику смысловых акцентуаций, но и выявить ассоциативные
доминанты русской языковой личности. Наличие ассоциативных доминант на
протяжении нескольких поколений испытуемых свидетельствует об устойчивости
определённых смысловых узлов – доминантных ассоциативных полей (см. об
ассоциативных доминантах <a href="#papers">Список публикаций</a>). 

<p>Подкорпусы создавались для решения конкретных задач в рамках локальных
исследований (см. ниже раздел <a href="#lsubcorp">Локальные подкорпусы</a>).

<p>В структуре основного корпуса ассоциаций и подкорпусов СИБАС две части: 1)
массив эмпирических (полученных экспериментальным путем) данных (в открытом
доступе для пользователя); 2) информативно-исследовательская часть, которая
включает в себя информацию об уже завершенных и текущих проектах, в которых
используются эмпирические материалы СИБАС. Обе части постоянно обновляются по
мере накопления новых данных и результатов реализации конкретных проектов. 

<p>Обновляемая часть СИБАС содержит новые экспериментальные данные, которые
находятся в процессе накопления и обработки. В настоящее время доступны для
исследования основной корпус СИБАС 1 и подкорпусы со всем встроенным
инструментарием обработки данных, а также новейшие материалы СИБАС 2 в формате
ассоциативных словарей.

<p>Для получения контекстов, в которых иллюстрируется использование конкретных
ассоциативных пар, возможен выход из СИБАС в Национальный корпус русского языка
(НКРЯ, <a href="http://www.ruscorpora.ru/new/">www.ruscorpora.ru/new/</a>). Для этого необходимо, войдя в
ассоциативное поле слова-стимула, кликнуть нужное слово-ассоциат. Эта операция
выведет на экран результаты поиска контекстов при наличии заданной конфигурации
соответствующих вербальных единиц в НКРЯ.

<h2>Из истории проекта</h2>
<p>Общероссийский проект создания нового русского ассоциативного тезауруса был
разработан в Институте языкознания РАН при поддержке РГНФ (грант № 12-04-
12059в «Информационная исследовательская база современного русского языка по
материалам массового ассоциативного эксперимента», руководитель: д-р филол.
наук, проф., руководитель сектора этнопсихолингвистики Института языкознания
РАН Наталья Владимировна Уфимцева). На создание такого словаря-тезауруса
нацелен наиболее крупный эксперимент, первый этап которого уже закончен и
отражен в созданных и опубликованных словарях (см. список публикаций ниже, а
также на сайте ИЯз РАН). С этого эксперимента начиналась работа над
региональными базами: СИБАС (Сибирь и Дальний восток), ЕВРАС (Европейская часть
России) и УрРАС (Уральский регион).

<p>Представленные в опубликованных книжных версиях словарей данные
соответствуют результатам эксперимента 2008 – 2013 гг. По мере продолжения
экспериментальной работы и аккумуляции новых данных региональные словари
(ЕВРАС, УрРАС и СИБАС) будут включать в себя результаты трех этапов массового
ассоциативного эксперимента.

<h3>Первый этап эксперимента: СИБАС – 1</h3>
<p>Результаты первого этапа общероссийского ассоциативного эксперимента в
азиатских регионах РФ в полной мере доступны пользователю (всего было получено
5011 анкет) в основном корпусе СИБАС.

<p><i>Подготовка эксперимента</i>. В Институте языкознания РАН для восточных
регионов страны был подготовлен список стимулов первого эксперимента и 5000
анкет (при технической компьютерной поддержке Г.А. Черкасовой) по сто стимулов
в каждой (список стимулов для этого этапа составил 1000 единиц).

<p><i>Проведение эксперимента</i>. Основной эксперимент проводился с 2008 по
2013 год в наиболее крупных городах азиатской части России – Новосибирске,
Омске, Красноярске, Томске и Северске, Новокузнецке, Барнауле, Кемерово,
Владивостоке, Хабаровске, Чите и Иркутске. Небольшая часть анкет после
отбраковки была заменена новыми в проведенном для этой цели дополнительном
эксперименте 2012–2013 годов. Экспериментальные материалы направлялись в каждый
регион с учетом демографических особенностей на местах (общее количество
проживающих в регионе и доля студентов). В качестве испытуемых выступали
студенты преимущественно в следующих возрастных категориях: основная масса от
17 до 19 лет; значительная доля испытуемых (от одной шестой до одной четверти в
разных регионах) в возрасте от 20 до 22 и небольшое количество от 23 до 25 лет.
Единичные случаи – в возрасте 16 лет или старше 25, но не старше 30 лет. Все
испытуемые – студенты. В базе имеются данные о половой принадлежности
испытуемых, об их профессиональной специализации и о конкретных регионах –
местах проживания и обучения.

<p>В азиатской части эти экспериментальные данные аккумулировались и
обрабатывались сначала на кафедре истории и типологии языков и культур
Новосибирского государственного университета (ранее в лаборатории
этнопсихолингвистики НИЧ НГУ), в настоящее время в секторе русского языка в
Сибири Института филологии СО РАН. Экспериментальная работа в азиатской части
была организована на базе НГУ и ИФЛ СО РАН с участием ряда крупных вузов Сибири
и Дальнего Востока (см. информацию о региональных участниках проекта).

<h3>Второй этап эксперимента: СИБАС – 2</h3>
<p>СИБАС – 2 создан по результатам второго массового ассоциативного
эксперимента, проведенного в вузах Сибири и Дальнего Востока (2014 – 2021). В
отличие от СИБАС 1, который создан на основе свыше 1000 стимулов в результате первого
массового ассоциативного эксперимента (2008 – 2013), СИБАС 2 получен на 4000
стимулов, список которых формировался из реакций, полученных в ходе первого
эксперимента по созданию СИБАС 1.

