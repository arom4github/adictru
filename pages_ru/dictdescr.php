<h2>Подкорпус ассоциаций военных – ПВАС (2015 – 2018)</h2>
<p>Данные подкорпуса собирались в ходе свободного ассоциативного
эксперимента, который проводился с 10 ноября по 4 декабря 2015 года в
<b>Новосибирском военном институте им. генерала армии И. К. Яковлева
внутренних войск МВД России</b> (далее по тексту – НВИ). Название вуза
приводится здесь таким, каким оно было во время экспериментальной
работы. Срок обучения – 5 лет, форма обучения – очная, на базе среднего
(полного) общего образования.
<p>
В качестве испытуемых выступали курсанты преимущественно в
следующих возрастных категориях: основная масса от 18 до 22 лет;
значительная доля испытуемых в возрасте от 23 до 25 и небольшое
количество в возрасте 17 лет. Все испытуемые – лица мужского пола.
<p>
В эксперименте участвовали 993 курсанта. После первичной обработки
материала были отбракованы 165 анкет. Таким образом, настоящий
подкорпус содержит данные с <b>828</b> анкет.
<p>
В институте обучаются граждане России, прибывшие из всех регионов
страны. Подавляющее большинство испытуемых (62 %) проживали до
поступления в институт в Сибирском федеральном округе, преимущественно
в Новосибирской области, Алтайском крае, Кемеровской области,
Красноярском крае, Омской области.
<p>
90 % испытуемых являются носителями русского языка. 18% испытуемых
обозначили себя в анкетах как билингвы. Среди билингвов отмечены
носители таких языков, как (в скобках указано количество испытуемых):
бурятский (8), осетинский (7), татарский (7), алтайский (7), даргинский (7),
лезгинский (7), табасаранский (7), кабардинский (6), казахский (4), хакасский
(4), аварский (3), чеченский (3), башкирский (2), киргизский (2). Кроме того,
среди испытуемых-билингвов было по одному представителю от носителей
следующих языков: агульского, адыгейского, карачаевского, кумыкского,
рутульского, таджикского, тувинского, черкесского, чувашского.
<p>
Данный проект изначально организовывался с целью получить
эмпирический материал для ассоциативно-вербальной модели
профессиональной идентификации военного. Исследование проводилось
Романом Анатольевичем Кафтановым в секторе русского языка в Сибири
ИФЛ СО РАН под руководством главного научного сотрудника сектора, д-ра
филол. наук, проф. Ирины Владимировны Шапошниковой. С определенной
долей допущений ассоциативно-вербальную модель профессиональной
идентичности можно представить в виде проекции сознания, которое, в свою
