<h2>Подкорпус вербальных ассоциаций носителей якутского языка</h2>
<p>Подкорпус вербальных ассоциаций носителей якутского языка (сокращенно
<i>якутский подкорпус</i>) был создан для решения конкретных задач в рамках локального
исследования сотрудника Лаборатории вербальных культур Сибири и Дальнего
Востока ИФЛ СО РАН Савицкой С.В., посвященного сопоставительному анализу
образов сознания, входящих в семантическую категорию «государственность» и
выявлению их этнокультурной специфики в языковом сознании якутов и русских. 
<p>Эмпирический материал для якутского подкорпуса был получен в ходе свободного
ассоциативного эксперимента, проведенного в г. Мирный Республики Саха (Якутия)
в декабре 2018 г. В эксперименте в качестве испытуемых выступили студенты
Мирнинского Политехнического института (филиала) Северо-Восточного Федерального
Университета (СВФУ), всего 350 чел. После обработки результатов эксперимента,
валидными были признаны 340 анкет, из них 248 – от испытуемых-носителей
якутского языка и 92 – от испытуемых, которые обозначили своим родным языком
(или языком основного общения) русский (в этой группе участников были как
русские по национальности, так и якуты – жители городов республики, которые
традиционно являются русскоязычными). Якутский подкорпус был сформирован на
базе 248 анкет, полученных от испытуемых-носителей якутского языка.
<p>Гендерная принадлежность и возраст участников эксперимента: из 340
респондентов 210 чел. – мужчины, что составляет 62% от общего количества
участников эксперимента, 122 чел. – женщины, что составляет 36% от общего
числа,  8 чел. (2%) не указали пол. Такая гендерная диспропорция объясняется
тем, что Мирнинский Политехнический Институт (МПТИ) готовит в основном кадры
для алмазодобывающей и нефтегазовой отраслей Якутии – секторов традиционно
мужской занятости. Большинство студентов МПТИ обучаются на факультетах «Горное
дело» и «Нефтегазовое дело». 296 респондентов (87% от общего числа) относятся к
возрастной группе 18-22 года, 33 респондента (9%) относятся к возрастной
категории 23-24 года. Кроме сведений о гендерной принадлежности и возрасте, на
сайте  СИБАС также есть дополнительные данные о профессиональной специализации
и географии респондентов.
<p>Анкета свободного ассоциативного эксперимента была подготовлена на двух
языках, якутском и русском, и содержит одинаковый набор слов-стимулов. Они были
подобраны с учетом тематики исследования (категория «государственность») и
представляют не только различные классы слов (существительные, прилагательные,
глаголы и др.), но и разные сферы употребления. Так, например, стимулы <b>дом,
человек, флаг, народ, любить, свободный</b> относятся к общеупотребительной
лексике, тогда как стимулы <b>коррупция, политик, реформа, государство, министр</b>
являются терминами и характеризуются ограниченной сферой употребления. Полный
список слов-стимулов для каждого языка можно найти во вкладке <b>Словари</b> вверху
страницы (раздел <b>Стимулы</b>). Якутский подкорпус, равно как и основной корпус и
другие подкорпусы СИБАС, доступен для использования в формате прямого и
обратного словарей. Словарные статьи якутского подкорпуса построены по такому
же принципу, что и в основном корпусе СИБАС, поэтому статистические данные
приводятся в аналогичном порядке (подробнее см. раздел <b>О словаре</b>).
Дополнительный функционал СИБАС (частеречная разметка*; функция поиска стимулов
и реакций) также применим к якутскому подкорпусу. 
<p><i>*частеречная разметка в якутском подкорпусе осуществляется в отношении реакций только на русском языке </i>


