стимул (501). Вторая цифра указывает на число разных ответов (т.е. ответов
разными словами) в данной статье (131); третья цифра показывает, сколько
испытуемых оставили стимул без ответа (6); последняя цифра дает число единичных
реакций (каждая из них дана только одним испытуемым) (93).
