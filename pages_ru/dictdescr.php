<p>    В ней реакции размещаются в порядке убывания их частотности, после
каждой реакции стоит цифра, обозначающая число людей, ответивших данным словом
на стимул БРАТ. После каждой такой цифры стоит точка с запятой (;). Несколько разных
реакций с одним и тем же индексом частотности расположены в алфавитном порядке,
и отделяются друг от друга точкой с запятой, единичные реакции также располагаются списком в
алфавитном порядке в конце словарной статьи и разделяются точкой с запятой.
Аналогичным образом реакции, превышающие объем одной словоформы и содержащие
знаки препинания, разделяются точкой с запятой. Завершается словарная статья
итоговыми цифрами с разделительным знаком (,) (запятая) (501, 131, 6, 93).
