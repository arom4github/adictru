<h2>Подкорпус вербальных ассоциаций носителей русского языка в Казахстане РКАС (2017 – 2020)</h2>
<p>
Данный подкорпус создавался в рамках локального сравнительно-
сопоставительного исследования процессов вестернизации как
социокультурного и психолингвистического феномена на основе их
отражения в русском языковом сознании молодых людей, проживающих в
азиатских регионах России и Казахстане. Исследование выполнялось
аспирантом Новосибирского государственного университета Евгенией
Викторовной Бентя под руководством д-ра филол.наук, профессора, главного
научного сотрудника сектора русского языка в Сибири ИФЛ СО РАН Ирины
Владимировны Шапошниковой. Сравнительно-сопоставительный анализ
базировался на использовании в качестве эталонных для азиатского региона
материалов СИБАС (2008-2013), а также собранных Е.В.Бентя эмпирических
материалов в результате свободного ассоциативного эксперимента,
проведенного ею в 2017 году в казахстанских вузах. Цель создания
подкорпуса определила список стимулов. Бóльшая их часть – 98 единиц –
совпадают со стимулами СИБАС (2008-2013), на которые испытуемыми-
россиянами было дано наибольшее количество реакций, отсылающих к
реалиям западной культуры. Два не вошедших в список СИБАС (2008-2013)
стимула – европейский и американский – были включены в анкеты
казахстанского эксперимента для более четкого определения ассоциативного
ряда, связанного с западным миром (отношение, характер контактов и т.п.).
Эти стимулы напрямую отсылают испытуемых к рассматриваемым реалиям.
Несмотря на конкретную локальную цель создания подкорпуса, его
ассоциативный словник может быть также использован в других
исследовательских целях.
<p>
Одна из особенностей представления материала в базе РКАС
заключается в том, что ошибки в реакциях на латинице не подвергались
коррекции, потому что особенности написания этих реакций на иностранном
языке могут дать дополнительную информацию об уровне владения этим
языком, либо указать на канал поступления данного слова (слов) в языковое
сознание испытуемого.
<p>
Материал подкорпуса собирался с помощью свободного
ассоциативного эксперимента по методике аналогичной той, что
применялась ранее для СИБАС (см. главную страницу СИБАС).
Эксперимент был проведен в шести городах Казахстана: Астане, Кокшетау,
Петропавловске, Павлодаре, Караганде и Костанае. Выбор городов был
мотивирован их географической принадлежностью к северным регионам
страны, где плотность русского населения наиболее высока, что в данном
случае также означает наличие бóльшего количества носителей русского
языка, как среди русских, так и среди других национальностей Казахстана.
Караганда расположена в центральном регионе Казахстана (и представляет
собой его административный центр, будучи при этом четверным
крупнейшим городом страны). Этот город был также включен в наш список в
силу его статуса и близости к северному региону, что способствует
появлению бóльшего количества жителей, для которых русский язык родной.
<p>
Эксперимент проводился в апреле-октябре 2017 года со студентами
следующих казахстанских вузов: Евразийский национальный университет
им. Л. Н. Гумилева (Астана), университет «Туран-Астана» (Астана),
