<h3>Редактирование словаря и степень вмешательства редактора</h3>
<p> Сбор данных и экспериментальная работа проводились при участии большого
количества людей в разных регионах, удаленных друг от друга на тысячи
километров. Некоторые участники помогали в сборе информации в течение очень
малого периода времени (месяца или двух), переключая в дальнейшем свое внимание
на другие дела. При этом не только технические возможности, но и условия
работы, а также понимание конкретных задач, стоящих перед участниками
эксперимента, могли сильно отличаться у участников экспериментальной работы в
разных регионах, соответственно и осуществить с первого раза полный текущий контроль над
отдельными участками работы не представлялось возможным в равной мере в разных
регионах. Поэтому иногда приходилось отбраковывать довольно большое количество
материалов и заново организовывать работу в некоторых регионах. Иногда
повторный эксперимент приходилось проводить уже в другом регионе (по разным
причинам, вплоть до банальных, когда доступ к аудитории студентов был затруднен
из-за непреодолимых административных барьеров). Это наложило некоторый отпечаток и на
итоговую статистику (см. таблицу ниже), которая в допустимых пределах отличается от
исходных планов, где предполагался несколько более равномерный учет данных в зависимости
от демографических особенностей каждого региона. Так, например, из Красноярска
изначально предполагалось получить не менее 660 анкет. В дальнейшем после
отбраковки большого количества анкет, заполненных с нарушением основных правил
проведения ассоциативного эксперимента (даже и при повторной отчаянной попытке
руководителей группы на месте провести эксперимент в этом городе), пришлось
дополнить недостающие данные  соответствующими материалами из других регионов.  
<p>     В процессе перевода анкетных данных в электронный формат в разные годы
принимали участие студенты и преподаватели из разных вузов, все упомянутые выше
проблемы проявились и на этом этапе работы. Таким образом, ошибки «наборщика»
при обработке экспериментальных материалов оказались неизбежными. Ошибки такого рода 
были устранены при финальной проверке и подготовке анкет для
ввода в базу. Ошибки наборщика проявились в
отсутствии необходимых разделительных знаков в некоторых словарных статьях
иногда, наоборот, в избыточных немотивированных знаках, сопровождающих какую-то
реакцию. Так, в статье прямого словаря <b>БРАТ</b> была лишняя запятая и пробел перед
реакцией <b>сват</b>, а также орфографическая ошибка (<b>страрший</b>) в слове <b>старший</b>.
Упомянутые ошибки в данном случае допущены при переводе анкеты в электронный
формат, они выявлялись при сравнении бумажных анкет с их электронными
вариантами.
<p>    Обращает на себя внимание огромное количество примитивных
орфографических ошибок в анкетах. Некоторые грубые ошибки носят систематический
характер, так как встречаются в разных анкетах, полученных в разных регионах.
Сюда можно отнести: <b>комунизм</b>, <b>ренисанс</b>, <b>пеонерский</b>, <b>премета</b>, <b>вежлево</b>,
<b>преподователь</b>, <b>придмет</b>, <b>пачтальон</b>, <b>педжак</b>, <b>пазор</b>, <b>позновать</b>, <b>лудший</b>, <b>призедент</b>,
<b>сегарет</b>, <b>пропость</b>, <b>нищита</b>, <b>песатель</b>, <b>учоба</b>, <b>канфету</b>, <b>доллар</b>, <b>лож</b>, <b>каридор</b>,
<b>мелиция</b>, <b>вперет</b>, <b>писсимизм</b>, <b>шуршавый</b>, <b>окуратный</b> и мн. др. Некоторые слова и
вовсе трудно узнаваемы: <b>шарв</b> (шарф), <b>иликтричка</b> (электричка), <b>перок</b> (пирог),
<b>писемизм</b> (пессимизм), <b>в биде</b> (в беде) и др. Грубые ошибки этого типа были
устранены. Рассматривая орфографические ошибки, а также ошибки,
свидетельствующие о нарушении культурно-речевых норм, мы руководствовались
принципами, которые были приняты ранее авторами «Русского ассоциативного
словаря» и изложены Юрием Николаевичем Карауловым в послесловии к первому тому
(М.: ООО «Издательство Астрель»: ООО «Издательство АСТ», 2002. С. 766 – 768). В
соответствии с этими принципами все орфографические ошибки наборщика и
испытуемого (смотри примеры выше) подлежат устранению (так, в приведенной выше
словарной статье были исправлены ошибки в словах-реакциях <b>старший</b> (<b>страрший</b>),
<b>тамбовский волк</b> (<b>томбовский волк</b>), <b>помощь</b> (<b>помощ</b>). Вместе с тем вмешательство редактора в целом ряде
случаев, где проявляется индивидуальное творчество испытуемого в ущерб
нормативности речи, должно быть минимальным, неискажающим
результаты эксперимента, способные дать новые знания пользователю (в том числе
