<p>     Словоформа (<b>брат</b>), которая задает словарную статью, является реакцией
на все те слова-стимулы, которые перечислены в словарной статье (<b>сестра</b>, <b>родной</b>
и т.д.). Цифровое обозначение здесь показывает частоту реагирования словом
(<b>брат</b>) на каждое предшествующее этой цифре слово-стимул. Так, слово <b>сестра</b> 104
раза (у 104 испытуемых) вызвало реакцию <b>брат</b>, а 22 испытуемых отреагировали на
стимул <b>родной</b> словом <b>брат</b>. Итоговые цифры в конце словарной статьи (303, 82)
указывают на общее количество появлений слова <b>брат</b> в качестве реакции в базе
данных (303) и на общее число разных слов-стимулов, вызвавших реакцию <b>брат</b>
(82).
<p><em>Обратный словарь</em> позволяет получить информацию об ассоциативных
доминантах (словах, имеющих наибольшее число ассоциативных связей) в языковом
сознании русских, проживающих в азиатских регионах России. Эта информация
организована следующим образом. При обращении к обратному словарю можно выбрать
опцию «по количеству реакций», которая ранжирует слова-реакции в зависимости от
того, сколько раз ими реагировали испытуемые. Все слова-реакции распределяются
в соответствии со следующими показателями: от 10000 раз до 2000; от 1999 раз до
1500; от 1499 до 1000; от 999 до 750; от 749 до 500; от 499 до 250; от 249 до
1. Так при запросе «от 10000 до 2000» получаем результаты отбора с лидирующими
словами-реакциями <b>человек</b> (10012), <b>деньги</b> (4037), <b>дом</b> (2584) и <b>друг</b> (2343).
Этими словами испытуемые отреагировали такое количество раз, которое указано
здесь в скобках. Все эти цифровые показатели находятся в пределах от 11000 до
2000 (не более и не менее). 
<p>     Опция «по количеству стимулов» позволяет выделить ассоциативные
доминанты на основе общего количества разных слов-стимулов, вызвавших
конкретные слова-реакции. Здесь реакции ранжируются по следующим показателям:
от 600 до 200; от 199 до 150; от 149 до 100; от 99 до 50; от 49 до 1. Так,
выбрав максимальные показатели (от 600 до 200), мы получаем 10 реакций-лидеров,
которые связаны с наибольшим количеством разных слов-стимулов. Это - <b>человек</b>
(450), <b>жизнь</b> (345), <b>дом</b> (335), <b>деньги</b> (263), <b>хорошо</b> (261),  <b>плохо</b> (260), <b>друг</b>
(253), <b>нет</b> (235), <b>мир</b> (232), <b>я</b> (230). Слово <b>человек</b> было реакцией на 450 разных
слов-стимулов, <b>жизнь</b> на 345 разных слов-стимула и т.д.
<p>     Какая-то часть реакций была представлена не вербально, то есть цифрами
и знаками, были также реакции словами из других (иностранных для русского
человека) языков. Доступ к словарным статьям реакций этого вида осуществляется
через опцию «в алфавитном порядке реакций», далее см. знаки перед списком букв.

